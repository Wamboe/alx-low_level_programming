#include "3-calc.h"

/**
 * op_add - adds ints
 * @a: num1
 * @b: num2
 * Return: result of add
 */
int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - subtracts ints
 * @a: num1
 * @b: num2
 * Return: result of sub
 */
int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - multiplies ints
 * @a: num1
 * @b: num2
 * Return: result of mul
 */
int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - divides ints
 * @a: num1
 * @b: num2
 * Return: result of div
 */
int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - modulus ints
 * @a: num1
 * @b: num2
 * Return: result of mod
 */
int op_mod(int a, int b)
{
	return (a % b);
}
